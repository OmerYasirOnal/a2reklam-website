#!/usr/bin/env node
/**
 * gen-hero-sizes.mjs — src/utils/heroImageSizes.ts'i üretir.
 * public/assets/img altındaki TÜM "-960.webp" görsellerinin gerçek piksel boyutunu
 * (sharp metadata) okuyup HERO_SIZES haritasını yazar. Hero görselleri (blog/ilçe/sektör/
 * hizmet) bu haritadan width/height alır → CLS önlenir. process-images.js çalıştıktan sonra
 * bunu da çalıştır:  node scripts/gen-hero-sizes.mjs
 */
import fs from 'fs';
import path from 'path';
import sharp from 'sharp';

const IMG_ROOT = path.join(process.cwd(), 'public', 'assets', 'img');
const OUT = path.join(process.cwd(), 'src', 'utils', 'heroImageSizes.ts');

function walk(dir) {
  const out = [];
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, e.name);
    if (e.isDirectory()) out.push(...walk(full));
    else if (/-960\.webp$/i.test(e.name)) out.push(full);
  }
  return out;
}

const files = fs.existsSync(IMG_ROOT) ? walk(IMG_ROOT).sort() : [];
const entries = [];
for (const f of files) {
  try {
    const { width, height } = await sharp(f).metadata();
    const url = '/' + path.relative(path.join(process.cwd(), 'public'), f).split(path.sep).join('/');
    entries.push(`  "${url}": { w: ${width}, h: ${height} },`);
  } catch (e) {
    console.warn('skip', f, e.message);
  }
}

const body = `// AUTO-GENERATED by scripts/gen-hero-sizes.mjs — elle düzenleme.
// public/assets/img altındaki tüm -960.webp görsellerinin gerçek piksel boyutu.
// CLS önleme: hero template'leri (ResponsiveHeroImage) img'e width/height verir → layout shift sıfır.
// Yeni görsel eklenince: node scripts/process-images.js && node scripts/gen-hero-sizes.mjs
export const HERO_SIZES: Record<string, { w: number; h: number }> = {
${entries.join('\n')}
};
`;

fs.writeFileSync(OUT, body);
console.log(`Wrote ${entries.length} hero sizes -> ${path.relative(process.cwd(), OUT)}`);
